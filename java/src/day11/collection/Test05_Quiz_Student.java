package day11.collection;

public class Test05_Quiz_Student {
	private int num;
	private String name;
	private String call;

	public Test05_Quiz_Student(int num, String name, String call) {
		this.num = num;
		this.name = name;
		this.call = call;
	}

	public int getNum() {
		return num;
	}

	public String getName() {
		return name;
	}

	public String getCall() {
		return call;
	}
}
